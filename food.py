from turtle import Turtle
import random


class Food(Turtle):

    # Initializes a blue circle shape turtle as the object of these class is created
    def __init__(self):
        super().__init__()
        self.shape("circle")
        self.penup()
        self.shapesize(stretch_len=0.5, stretch_wid=0.5)
        self.color("blue")
        self.speed("fastest")
        self.refresh()

    # Moves the blue circle shape turtle at a random spot
    def refresh(self):
        random_x = random.randint(-280, 280)
        random_y = random.randint(-280, 280)
        self.goto(random_x, random_y)
